    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        
        String name = tf_name.getText();
        ta_result.setText("");
        logic.addData(list, name + "\n");
        list.forEach(item -> ta_result.append(item.toString()));
        
        tf_name.setText("");
        
        if (!(name.matches("[a-zA-Z0-9]+"))){
            ta_result.setText("Please Try Again");
        }
        
        if (!(name.matches("[a-zA-Z0-9]+"))){
            JOptionPane.showMessageDialog(rootPane, "Please input "
                    +"alphanumeric name only And Try Again", "Messege", HEIGHT);
        }
    }//GEN-LAST:event_btn_addActionPerformed

    private void btn_checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_checkActionPerformed
        
        String email = tf_email.getText();
        
        Boolean check_email = logic.checkEmail(email);
        
        String result = "Invalid Email Format!";
        
        if(check_email){
            result = "Your Email Format Was Valid!";
        }
        label_result.setText(result);
    }//GEN-LAST:event_btn_checkActionPerformed

